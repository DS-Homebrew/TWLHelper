import discord
from discord.ext import commands
from utils.utils import is_staff


class Mod(commands.Cog):
    """
    Moderator-only commands.
    """
    def __init__(self, bot):
        self.bot = bot

    async def cog_check(self, ctx):
        if ctx.guild is None:
            raise commands.NoPrivateMessage()
        return True

    @commands.command()
    @commands.check_any(commands.is_owner(), is_staff())
    async def load(self, ctx, *, module: str):
        """Loads a Cog."""
        try:
            if module[0:7] != "cogs.":
                module = "cogs." + module
            self.bot.load_extension(module)
            await ctx.send('‚úÖ Extension loaded.')
        except Exception as e:
            await ctx.send(f'üí¢ Failed!\n```\n{type(e).__name__}: {e}\n```')

    @commands.command()
    @commands.check_any(commands.is_owner(), is_staff())
    async def unload(self, ctx, *, module: str):
        """Unloads a Cog."""
        try:
            if module[0:7] != "cogs.":
                module = "cogs." + module
            if module == "cogs.load":
                await ctx.send("‚ùå I don't think you want to unload that!")
            else:
                self.bot.unload_extension(module)
                await ctx.send('‚úÖ Extension unloaded.')
        except Exception as e:
            await ctx.send(f'üí¢ Failed!\n```\n{type(e).__name__}: {e}\n```')

    @commands.command(name='reload')
    @commands.check_any(commands.is_owner(), is_staff())
    async def reload(self, ctx, *, module: str):
        """Reloads a Cog."""
        try:
            if module[0:7] != "cogs.":
                module = "cogs." + module
            self.bot.reload_extension(module)
            await ctx.send('‚úÖ Extension reloaded.')
        except Exception as e:
            await ctx.send(f'üí¢ Failed!\n```\n{type(e).__name__}: {e}\n```')

    @commands.command()
    @commands.check_any(commands.is_owner(), is_staff())
    async def speak(self, ctx, channel: discord.TextChannel, *, inp):
        await channel.send(inp, allowed_mentions=discord.AllowedMentions(everyone=True, roles=True))


def setup(bot):
    bot.add_cog(Mod(bot))
